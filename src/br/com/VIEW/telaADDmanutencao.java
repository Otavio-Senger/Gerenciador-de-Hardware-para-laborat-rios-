    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed

        String IDtext = txtIDmanutencao.getText();
        String dataString = txtData.getText();
        String hora = txtHora.getText();
        String tipo = (String) caixaTipo.getSelectedItem();
        String status = (String) caixaStatus.getSelectedItem();
        String Id_eq = (String) caixaIDeq.getSelectedItem();

        SimpleDateFormat formatador = new SimpleDateFormat("dd/MM/yyyy");
        java.sql.Date dataSQL = null;

        if (IDtext.isEmpty() || dataString.isEmpty() || hora.isEmpty() || tipo.equals("Selecione o tipo") || status.equals("Selecione o status") || Id_eq.equals("Selecione o ID da máquina")) {
            JOptionPane.showMessageDialog(null, "Todos os campos devem ser preenchidos!!!");
        }

        int ID = Integer.parseInt(IDtext);
       

        try {
            Date data = formatador.parse(dataString);
            dataSQL = new java.sql.Date(data.getTime());

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data inválida. Formato esperado: dd-MM-yyyy", "Erro", JOptionPane.ERROR_MESSAGE);
        }

        manutencaoDTO mdto = new manutencaoDTO();
        mdto.setId_mn(ID);
        mdto.setData_mn(dataSQL);
        mdto.setHora_mn(hora);
        mdto.setTipo_mn(tipo);
        mdto.setStatus_mn(status);
        maquinaDTO maq = new maquinaDTO();
        maq.setId_eq(Integer.parseInt(Id_eq));
        mdto.setMaquina(maq);

        manutencaoDAO mdao = new manutencaoDAO();
        mdao.adicionar(mdto);

//          maquinaDTO maqdto = new maquinaDTO();
//          maqdto.setStatus(tipo);

    }//GEN-LAST:event_btnAdicionarActionPerformed

